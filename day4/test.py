input_data = """MMMSXXMASM
MSAMXMSMSA
AMXSXMAAMM
MSAMASMSMX
XMASAMXAMM
XXAMMXXAMA
SMSMSASXSS
SAXAMASAAA
MAMMMXMMMM
MXMXAXMASX"""

mat = [list(row) for row in input_data.split("\n")]

hits = []
for rowIndex, row in enumerate(mat):
    hits.append(
        [[rowIndex, colIndex] for colIndex, value in enumerate(row) if value == "X"]
    )

def get_input_char(pos):
    row, col = pos
    try:
        return mat[row][col]
    except IndexError:
        return None

WORD = "XMAS"
word_hits = 0
xmas_hits = []

for row, col in [hit for sublist in hits for hit in sublist]:
    word_arr_mat = [
        [[row, col + i] for i in range(4)],
        [[row, col - i] for i in range(4)],
        [[row - i, col] for i in range(4)],
        [[row + i, col] for i in range(4)],
        [[row + i, col + i] for i in range(4)],
        [[row - i, col - i] for i in range(4)],
        [[row - i, col + i] for i in range(4)],
        [[row + i, col - i] for i in range(4)],
    ]

    arr = [
        [{"pos": ch_pos, "char": get_input_char(ch_pos)} for ch_pos in row]
        for row in word_arr_mat
    ]

    for word in arr:
        char_word = "".join([w["char"] for w in word if w["char"]])
        if char_word == WORD:
            xmas_hits.append(word)

WORD2 = "MAS"
mas_hits = []

for hit in xmas_hits:
    row, col = hit[2]["pos"]

    check1 = [
        [row + 1, col - 1],
        [row, col],
        [row - 1, col + 1],
    ]

    check2 = [
        [row + 1, col + 1],
        [row, col],
        [row - 1, col - 1],
    ]

    word1_bits = [get_input_char(ch_pos) for ch_pos in check1]
    word2_bits = [get_input_char(ch_pos) for ch_pos in check2]

    word1 = "".join(filter(None, word1_bits))
    word1_reverse = "".join(reversed(list(filter(None, word1_bits))))

    word2 = "".join(filter(None, word2_bits))
    word2_reverse = "".join(reversed(list(filter(None, word2_bits))))

    if (
        (word1.startswith(WORD2))
        and
        (word2.startswith(WORD2))
    ):
        print("FOUND", hit)
        mas_hits.append(hit)

    if (
        (word1_reverse.startswith(WORD2))
        and
        (word2_reverse.startswith(WORD2))
    ):
        print("FOUND", hit)
        mas_hits.append(hit)

